function changeFlowerColor() {
  var x = document.getElementById("select").value;
  // document.getElementById("select").innerHTML = "You selected: " + x;
  console.log(x);
  if (x === 'red') {document.getElementById("mainFlower").innerHTML = redColor;}
  else if(x === 'pink') {document.getElementById("mainFlower").innerHTML = pinkColor;}
  else if(x === 'orange') {document.getElementById('mainFlower').innerHTML = orangeColor;}
  else if(x === 'yellow') {document.getElementById('mainFlower').innerHTML = yellowColor;}
  else if(x === 'blue') {document.getElementById('mainFlower').innerHTML = blueColor;}
  else if(x === 'white') {document.getElementById('mainFlower').innerHTML = whiteColor;}
}